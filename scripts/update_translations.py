# Find each language block and inject foundation after footer
for lang, foundation_content in foundations.items():
    # Regex to find: language name, its object, down to footer object ending with rights
    pattern = re.compile(r"(" + lang + r": \{.*?footer: \{.*?rights: '© 2026 Hebron Automotive Pvt Ltd.',\n\s*\})", re.DOTALL)
    
    match = pattern.search(content)
    if match:
        original = match.group(1)
        replacement = original + ",\n" + foundation_content
        content = content.replace(original, replacement)

with open(translations_path, "w", encoding="utf-8") as f:
    f.write(content)

print("Translations updated successfully.")
